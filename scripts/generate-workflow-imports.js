#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const WORKFLOWS_SRC = path.join(process.cwd(), 'src', 'workflows');
const OUT_FILE = path.join(WORKFLOWS_SRC, '__workflow_imports.ts');

function findWorkflowDirs() {
  try {
    const entries = fs.readdirSync(WORKFLOWS_SRC, { withFileTypes: true });
    return entries
      .filter((d) => d.isDirectory() && !d.name.startsWith('.'))
      .map((d) => d.name)
      .filter((name) => {
        const folder = path.join(WORKFLOWS_SRC, name);
        const candidates = ['index.ts', 'index.tsx', 'index.js', 'index.mjs', 'index.jsx'];
        return candidates.some((f) => fs.existsSync(path.join(folder, f)));
      });
  } catch (err) {
    console.error('[generate-workflow-imports] Failed to read workflows directory:', err.message);
    process.exit(1);
  }
}

function generate() {
  const dirs = findWorkflowDirs().sort();

  const lines = [];
  lines.push('// This file is generated by scripts/generate-workflow-imports.js');
  lines.push('// DO NOT EDIT BY HAND');
  lines.push('');
  lines.push("export const WORKFLOW_IMPORTS: Record<string, () => Promise<any>> = {");

  for (const name of dirs) {
    lines.push(`  '${name}': () => import('./${name}/index'),`);
  }

  lines.push('};');
  lines.push('');
  lines.push('export default WORKFLOW_IMPORTS;');
  lines.push('');

  try {
    fs.writeFileSync(OUT_FILE, lines.join('\n'), 'utf8');
    console.log(`[generate-workflow-imports] Wrote ${OUT_FILE} (${dirs.length} entries)`);
  } catch (err) {
    console.error('[generate-workflow-imports] Failed to write import file:', err.message);
    process.exit(1);
  }
}

generate();
